<h3>Program Description</h3>

<img src="{{ asset('front_assets/img/post-graduate.jpg') }}">

<p>The program is ideally suited to international students who already hold a minimum three-year degree in information technology or computer science. Advanced study in business management will enhance employability for entry and middle management positions in various information technology professions in Canada, depending on prior educational experience.</p>

<p>This multi-disciplinary program includes business management, communications, and specialized information technology courses. For international students with a three-year degree in information technology or a related field, the program introduces management concepts in a Canadian context, with emphasis on internal and external communications, organizational behaviour, human resource management, and project management.</p>

<h4>Career Prospects</h4>

<p>The objective of this program is to enhance the employability of internationally-trained information technology graduates as they transition into information technology careers in Canada, with an emphasis on management positions</p>

<h3>Details</h3>

<ul>
<li><b>Location</b>: Fort St. John Campus</li>
<li><b>Length</b>: Four semesters (full time, depending on start date and academic progress)</li>
<li><b>Start</b>: September/January/May</li>
<li><b>Fees</b>: Please consult the International Education Department at info@careerclinic.com</li>
</ul>

<h3>Admission Prerequisites</h3>

<p>International students must have official transcripts demonstrating successful completion within the past five years of, at minimum, a three-year degree in information technology or computer science.</p>

<ul>
<li> A. Domestic students must have official transcripts demonstrating the following English Requirement: One of the following with a "B" grade or higher: Grade 12 English, English Literature 12, English 12 First Peoples, English 050, or English 099. Alternatively, any university-level English course with a "C" grade or higher. Students who do not meet one of the above English requirements must complete the Career Clinic Writing Assessment for appropriate course placement.</li>

<li> B. International students must have documentation demonstrating the following English Requirement: An IELTS with an overall score of 6.0 or higher with no band less than 6.0. Students with a writing band of 6.0 or less are required to complete English 099 with a "B" grade or higher before or during the first semester of their program. The necessity for upgrading can extend the length of the program.</li>
</ul>

