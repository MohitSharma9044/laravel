<h3>Accommodation</h3>

<p>Majority of programs arrange accommodations for students or they provide support to
students by rendering housing. Occasionally, students are required to find temporary housing
(if they arrive prior to the program date or choose to extend their stay) or sometimes students
need to find housing solutions for the entire program as well. Students need to be alert and
cautious about home-sharing networks and rely on verified accommodation websites, only.</p>
<p>There are a variety of accommodation that can be opted by candidates. Some of them are
listed below:</p>


<h3>On-Campus Accommodation</h3>

<p>International students are provided with choices to stay on campus. Majority of times, it
proves to be advantageous as students can live close by universities and have a safe
composed atmosphere to live in. Often, on campus accommodation encourages social
networking and leads to interaction with peers leading to multi-cultural interaction and value-
added experiences. It is often the cheapest form of accommodation. Some of the
disadvantages can be small rooms and sharing of rooms might degrade quality of experience
of living.</p>

<h3>Accommodation with Local Families</h3>

<p>Usually, international students are provided with this option by universities. Universities set-
up international students with suitable families. Accommodation rent included home-cooked
meals, making students feel less alone and less home-sick. Students are able to rely on elder

figures for guidance. Parents prefer this as well for the previously mentioned reasons and the
assured sense of safety that comes with living with local families.</p>


<h3>Questions to ask yourself before selecting an accommodation solution?</h3>

<ul>
<li>The place provides furnishing or not?</li>
<li>Are the living standards worth spending extra money?</li>
<li> Study desks for laptops and books, cupboards and other student-related furniture is
provided or not?</li>
<li> Is the kitchen ready with fuel, stove and separate utensils?</li>
<li> Is internet access provided, or it is charged separately in accommodation rents?</li>
<li> Meal prices are included in accommodation price structure?</li>
<li> What are the legal documents required for the purpose of rent lease?</li>
<li> Where are the nearest laundry services available?</li>
<li> Are there any hidden or extra costs to certain services?</li>
</ul>

